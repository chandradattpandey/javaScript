//push
let fruits1 = ["Apple", "Orange", "Plum"];
fruits1.push('mango');
console.log(fruits1);

//pop
let fruits2 = ["Apple", "Orange", "Plum"];
fruits2.pop();
console.log(fruits2);


// unshit
fruits2.unshift('mango', 'banana');
console.log(fruits2);


// shift
fruits2.shift();
console.log(fruits2);